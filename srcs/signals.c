#include "../include/minishell.h"

void	signal_handler(int sig)
{
	t_ext	*ext;

	if (sig == SIGINT)
	{
		get_ext()->exit_status = 130;
		ioctl(STDIN_FILENO, TIOCSTI, "\n");
		rl_replace_line("", 0);
		rl_on_new_line();
	}
}
